#include <unistd.h>
#include "libft.h"

void	ft_putnbr(int n)
{
	int i;

	i = 1;
	if (n == -2147483648)
		ft_putstr("-2147483648");
	else
	{
		if (n < 0)
		{
			write(1, "-", 1);
			n *= -1;
		}
		while ((n / i) > 9)
			i *= 10;
		while (i > 0)
		{
			ft_putchar((n / i) % 10 + 48);
			i /= 10;
		}
	}
}
